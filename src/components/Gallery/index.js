class Gallery extends Component {
  state = {activeThumbnail: imagesList[0].id}

  getActiveThumbnail = id => {
    this.setState({activeThumbnail: id})
  }

  render() {
    const {activeThumbnail} = this.state
    const activeImage = imagesList.filter(each => each.id === activeThumbnail)
    return (
      <div className="bg-container">
        <div className="gallery-container">
          <img
            src={activeImage[0].imageUrl}
            alt={activeImage[0].imageAltText}
            className="img"
          />
          <h1 className="heading">Nature Photography</h1>
          <p className="para">Nature Photography by Rahul</p>
          <ul className="image-card">
            {imagesList.map(eachItem => (
              <ThumbnailItem
                imageDetails={eachItem}
                key={eachItem.id}
                activeThumbnail={activeThumbnail}
                getActiveThumbnail={this.getActiveThumbnail}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

